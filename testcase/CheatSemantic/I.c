int abs(int z)
{
	if (z >= 0) return z;
	else return -z;
}
int abs2(int x, int y)
{
	if (x-y >= 0) return x-y;
	else return y-x;
}
int main()
{
	int i, j, a[10][10];
	float k;
	i;
	if (i <= 10)
		j = abs(i, k);
	else
		j = abs2(i);
	while (i < 10)
	{
		k = k + abs(1.0);
	}
	i = i + abs(a[j]);
	j;
}
